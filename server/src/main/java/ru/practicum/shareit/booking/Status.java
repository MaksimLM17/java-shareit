package ru.practicum.shareit.booking;
/**
 * Перечисление статусов бронирования в системе шеринга.
 * <p>
 * Определяет возможные состояния бронирования в течение его жизненного цикла.
 * Каждый статус отражает определенный этап обработки запроса на бронирование.
 * </p>
 *
 * <p><b>Жизненный цикл бронирования:</b></p>
 * <ol>
 *   <li><b>WAITING</b> → <b>APPROVED</b> или <b>REJECTED</b></li>
 *   <li><b>WAITING</b> → <b>CANCELLED</b> (отмена пользователем)</li>
 *   <li>Любой статус может быть отменен до начала бронирования</li>
 * </ol>
 *
 * <p><b>Правила изменения статусов:</b></p>
 * <ul>
 *   <li>Только владелец предмета может изменить WAITING → APPROVED/REJECTED</li>
 *   <li>Только автор бронирования может изменить WAITING → CANCELLED</li>
 *   <li>APPROVED и REJECTED являются конечными статусами (кроме отмены до начала)</li>
 * </ul>
 *
 * @author MaksimLM17
 * @version 1.0
 * @see ru.practicum.shareit.booking.model.Booking
 * @see ru.practicum.shareit.booking.BookingService
 * @since 2025
 */
public enum Status {
    /**
     * Ожидает подтверждения владельцем.
     * <p>
     * Начальный статус для всех новых бронирований.
     * Бронирование создано пользователем, но еще не обработано владельцем предмета.
     * </p>
     *
     * <p><b>Возможные переходы:</b></p>
     * <ul>
     *   <li>→ {@link #APPROVED} (владельцем)</li>
     *   <li>→ {@link #REJECTED} (владельцем)</li>
     *   <li>→ {@link #CANCELLED} (автором бронирования)</li>
     * </ul>
     *
     * <p><b>Бизнес-правила:</b></p>
     * <ul>
     *   <li>Предмет временно помечается как недоступный для других бронирований</li>
     *   <li>Владелец получает уведомление о новом запросе</li>
     *   <li>Автор бронирования может отменить запрос до подтверждения</li>
     * </ul>
     */
    WAITING,

    /**
     * Подтверждено владельцем.
     * <p>
     * Владелец предмета одобрил запрос на бронирование.
     * Бронирование активно и предмет будет передан арендатору в указанные даты.
     * </p>
     *
     * <p><b>Возможные переходы:</b></p>
     * <ul>
     *   <li>→ {@link #CANCELLED} (только до начала бронирования)</li>
     * </ul>
     *
     * <p><b>Бизнес-правила:</b></p>
     * <ul>
     *   <li>Предмет помечается как недоступный на период бронирования</li>
     *   <li>Автор бронирования может оставить комментарий после завершения</li>
     *   <li>Отмена возможна только до даты начала бронирования</li>
     * </ul>
     */
    APPROVED,

    /**
     * Отклонено владельцем.
     * <p>
     * Владелец предмета отклонил запрос на бронирование.
     * Бронирование не состоится, предмет доступен для других запросов.
     * </p>
     *
     * <p><b>Возможные переходы:</b></p>
     * <ul>
     *   <li>Нет возможных переходов (конечный статус)</li>
     * </ul>
     *
     * <p><b>Бизнес-правила:</b></p>
     * <ul>
     *   <li>Предмет немедленно становится доступным для новых бронирований</li>
     *   <li>Автор бронирования получает уведомление об отказе</li>
     *   <li>Статус не может быть изменен после установки</li>
     * </ul>
     */
    REJECTED,

    /**
     * Отменено пользователем.
     * <p>
     * Автор бронирования отменил свой запрос.
     * Бронирование аннулировано, предмет доступен для других запросов.
     * </p>
     *
     * <p><b>Возможные переходы:</b></p>
     * <ul>
     *   <li>Нет возможных переходов (конечный статус)</li>
     * </ul>
     *
     * <p><b>Бизнес-правила:</b></p>
     * <ul>
     *   <li>Отмена возможна только для бронирований в статусе {@link #WAITING}</li>
     *   <li>Для {@link #APPROVED} отмена возможна только до начала бронирования</li>
     *   <li>Предмет немедленно становится доступным для новых бронирований</li>
     *   <li>Владелец получает уведомление об отмене</li>
     * </ul>
     */
    CANCELLED;
}
